import com.bobo.waimai.pojo.Address;
import com.bobo.waimai.pojo.User;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by tianrun-bobo on 2018/2/23/17:03.
 */
public class ExtendAddress extends Address implements Serializable {
    private Integer totalMoney;

    public ExtendAddress(Integer addressId, String addressLianxi, String addressPhone, String addressName,
                         Integer userId, Date addressCreateTime, User user, Integer totalMoney) {
        super(addressId, addressLianxi, addressPhone, addressName, userId, addressCreateTime, user);
        this.totalMoney = totalMoney;
    }

    public ExtendAddress() {
    }

    @Override
    public String toString() {
        return "ExtendAddress{" +
                "totalMoney=" + totalMoney +
                "} " + super.toString();
    }

    public Integer getTotalMoney() {
        return totalMoney;
    }

    public void setTotalMoney(Integer totalMoney) {
        this.totalMoney = totalMoney;
    }
}
